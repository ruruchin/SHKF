/** Direct replies for greetings — no LLM required. */

function norm(text) {
  return String(text || '').toLowerCase().replace(/ё/g, 'е').replace(/[?!.,…]+$/g, '').replace(/\s+/g, ' ').trim();
}

export function getCasualChatReply(text) {
  const t = norm(text);

  if (/^(?:привет|здравствуй|здрав|хай|hello|хелло|йо|yo|салют|добр(?:ый|ое)\s+(?:день|утро|вечер))/.test(t)) {
    if (/как\s+дела|как\s+ты|как\s+у\s+тебя|как\s+сам|как\s+жизнь|как\s+настроение/.test(t)) {
      return 'Привет! У меня всё хорошо, я на связи. Как у тебя? Чем помочь — работа, дизайн или просто поболтать?';
    }
    return 'Привет! Я Konstancia, рада тебя видеть. Чем помочь?';
  }

  if (/как\s+дела|как\s+ты|как\s+у\s+тебя|как\s+сам|как\s+жизнь|как\s+настроение|чо\s+ты|чё\s+ты|че\s+ты/.test(t)) {
    return 'Нормально, я на связи и готова помочь. А у тебя как?';
  }

  if (/ты\s+тут|ты\s+здесь|на\s+связи/.test(t)) {
    return 'Да, я здесь. Пиши — отвечу.';
  }

  if (/^(?:спасибо|благодар|thanks)/.test(t)) {
    return 'Пожалуйста! Обращайся, когда понадоблюсь.';
  }

  if (/^(?:пока|до\s+свидан)/.test(t)) {
    return 'Пока! Буду здесь, когда снова понадоблюсь.';
  }

  if (/^(?:ок|окей|ага|угу|лол|хаха|класс|круто|понял|ясно|спс)[\s!?.]*$/.test(t)) {
    return 'Ок! Если что — я рядом.';
  }

  if (/konstancia|констанци/.test(t) && t.length <= 80) {
    return 'Я Konstancia — твой ассистент в SHKF. Могу помочь с Redmine, Figma, задачами и обычными вопросами.';
  }

  return 'Я здесь и готова помочь — с задачами, дизайном или простым вопросом. Что нужно?';
}
